int test_get_host_filter( char *host, char *user, char *match, int out )
{
    char *filter;
    int  result = 0;

    printf( "   get_host_filter( '%s', '%s', '%s', %i ) ", host, user, match, out );
    filter = get_host_filter( host, user );

    printf( " = '%s' ", filter );

    if ( filter == NULL ) {
        if ( match == NULL ) {
            result = 1;
        } else {
            result = 0;
        }
    } else {
        if ( match != NULL ) {
            if ( strcmp( filter, match ) == 0 ) {
                result = 1;
            }
            free( filter );
        }
    }

    if ( result == out ) {
        printf( " passed\n" );
        return( 0 );
    }
    printf( " failed\n" );
    return( 1 );
}

