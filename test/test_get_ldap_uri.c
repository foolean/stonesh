char *progname = "test_get_ldap_uri";

Options options;

int test_get_ldap_uri( char *uri ) {
    char *u;
    char *line;
    int  active;

    if ( uri == NULL ) {
        u = get_ldap_uri();    
        printf( "    get_ldap_uri() = '%s' - passed\n", u );
        free(u);
        return( 0 );
    }    

    active = 1;

    line = (char*)malloc(strlen(uri) + 10);
    memset(line, 0, strlen(uri) + 10);
    sprintf(line, "ldapuri %s", uri);
    initialize_options( &options );
    process_config_line(&options, line, "not_needed", 0, &active);
    fill_default_options(&options);
    free(line);

    u = get_ldap_uri();    
    printf( "    get_ldap_uri() = '%s' ", u );
    if ( strcasecmp(u, uri) == 0 ) {
        printf( "passed\n" );
        free_options(&options);
        free(u);
        return( 0 );
    }
    printf( "failed\n" );
    free(u);
    free_options(&options);
    return( 1 );

}

