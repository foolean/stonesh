char *progname = "test_get_ldap_base";

Options options;

int test_get_ldap_base( char *base ) {
    char *u;
    char *line;
    int  active;

    if ( base == NULL ) {
        u = get_ldap_base();    
        printf( "    get_ldap_base() = '%s' - passed\n", u );
        if ( u != NULL ) {
            free(u);
        }
        return( 0 );
    }    

    active = 1;

    line = (char*)malloc(strlen(base) + 15);
    memset(line, 0, strlen(base) + 15);
    sprintf(line, "ldapbase \"%s\"", base);
    initialize_options( &options );
    process_config_line(&options, line, "not_needed", 0, &active);
    fill_default_options(&options);
    free(line);

    u = get_ldap_base();    
    printf( "    get_ldap_base() = '%s' ", u );
    if ( strcasecmp(u, base) == 0 ) {
        printf( "passed\n" );
        free_options(&options);
        free(u);
        return( 0 );
    }
    printf( "failed\n" );
    free(u);
    free_options(&options);
    return( 1 );

}

